　少し離れた渡り廊下に何気なく立って、こっそりと見張り続ける。そうしていると、なんだか自分がすごくみじめに思えてきた。
（あーあ、なさけねー……）
　藤花は、だいたいクラスの二十番目ぐらいにやってきた。
　いつもの彼女と変わらない。別にヘンな帽子とかはかぶっていない。
