「いや、いいんだ。なんでもない。宮下の声がさ、ちょっと聞きたかったんだ。ごめん」
「ありがとうございます」
　彼女はバカ丁寧に言った。しかしこれもお袋さん対策で、実際には「嬉しいわ」と言ってるのだ。
　やっぱり、嫌われたわけではない。
「じゃ、また明日、学校で」
「はい、失礼します」
