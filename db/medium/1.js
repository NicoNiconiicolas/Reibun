「竹田だけど」
「はい」
　そっけない返事だ。しかしこれはお袋さんが横で聞き耳を立てているのだろう。
　宮下家は、今時親子電話でさえないらしい。
「こないだの日曜だけどさ、どっか出かけた？」
「いいえ、別に」
　と言いながら、彼女はこんこん、と受話器を叩いた。これはきっと、例のサインの二本指を立てるヤツをあらわしているのだろう。
〝ごめんなさい、今はちょっと〟だ。
　わかっている。でもそれでも訊いてみる。
「あのさ」
「はい」
「ブギーポップって知ってる？」
「え？」
