「それで、おまえらも何かそれらしい話を聞いたら、すぐに私に知らせろ。奴等の友人あたりで、家出先を聞いてる奴がいるかも知れないからな」
　僕らは返事をしない。これもいつものことだ。そして教師の方もかまわず話を続ける。
「それと、例の霧間凪が今日も登校していない。ヤツにはいつでも充分目を光らせておくように。あの女は陰で何をしているかわかったものじゃないからな」
