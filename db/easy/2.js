「あっちのほうだよ」
「うん」
「なんかのぼせてきちゃった」
「そろそろあがる？」
「だめだめ。ちーちゃんが流れ星見るまで。見てないのはちーちゃんだけなんだから」
「いいよ。先にあがってて」
「だめだよう。みんなで一斉に宇宙のエネルギーを取りこむんだから」
「そうだよ。ちーちゃんが見えるまで待ってる」
