「わたくしは今年中にあと十名の入会申込書を提出することをここにお約束いたします！」
「わたくしは残りの人生をかけて心の貧しい者を救うことをお約束いたします！」
「わたくしは来年の滝行ツアーに最低でも五回は参加することをお約束いたします！」
